#include "WindowHandles.h"

//----------------------------------------------------------------------------------------------------
WindowHandles::WindowHandles(::HDC* HDC, ::HWND* HWND)
{
    m_HDC = HDC;
    m_HWND = HWND;
}

//----------------------------------------------------------------------------------------------------
HWND* WindowHandles::HWND() const
{
    return m_HWND;
}

//----------------------------------------------------------------------------------------------------
HDC* WindowHandles::HDC() const
{
    return m_HDC;
}

//----------------------------------------------------------------------------------------------------
void WindowHandles::SetHDC(::HDC* hdc)
{
    m_HDC = hdc;
}

//----------------------------------------------------------------------------------------------------
WindowHandles::~WindowHandles()
{
    free(m_HDC);
    free(m_HWND);
}

