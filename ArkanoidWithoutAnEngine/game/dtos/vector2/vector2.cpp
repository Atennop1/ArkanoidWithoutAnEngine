#include "vector2.h"

//----------------------------------------------------------------------------------------------------
Vector2::Vector2()
{
    m_x = 0.0f;
    m_y = 0.0f;
}

//----------------------------------------------------------------------------------------------------
Vector2::Vector2(float x, float y)
{
    m_x = x;
    m_y = y;
}

//----------------------------------------------------------------------------------------------------
float Vector2::X() const
{
    return m_x;
}

//----------------------------------------------------------------------------------------------------
float Vector2::Y() const
{
    return m_y;
}

