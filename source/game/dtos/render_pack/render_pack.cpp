#include "render_pack.h"

//----------------------------------------------------------------------------------------------------
RenderPack::RenderPack()
{
    m_pen_ = CreatePen(PS_SOLID, 0, RGB(255, 255, 255));
    m_brush_ = CreateSolidBrush(RGB(255, 255, 255));
}

//----------------------------------------------------------------------------------------------------
RenderPack::RenderPack(COLORREF color)
{
    m_pen_ = CreatePen(PS_SOLID, 0, color);
    m_brush_ = CreateSolidBrush(color);
}

//----------------------------------------------------------------------------------------------------
RenderPack::RenderPack(HPEN pen, HBRUSH brush)
{
    m_pen_ = pen;
    m_brush_ = brush;
}

//----------------------------------------------------------------------------------------------------
HPEN RenderPack::Pen() const
{
    return m_pen_;
}

//----------------------------------------------------------------------------------------------------
HBRUSH RenderPack::Brush() const
{
    return m_brush_;
}

