#include "vector2.h"

//----------------------------------------------------------------------------------------------------
Vector2::Vector2()
{
    m_x = 0;
    m_y = 0;
}

//----------------------------------------------------------------------------------------------------
Vector2::Vector2(int x, int y)
{
    m_x = x;
    m_y = y;
}

//----------------------------------------------------------------------------------------------------
int Vector2::X() const
{
    return m_x;
}

//----------------------------------------------------------------------------------------------------
int Vector2::Y() const
{
    return m_y;
}

