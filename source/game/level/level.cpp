#include "level.h"

//----------------------------------------------------------------------------------------------------
Level::Level(std::array<std::array<char, 12>, 14> map, LevelView *level_view)
{
    m_map_ = map;
    m_level_view_ = level_view;
}

//----------------------------------------------------------------------------------------------------
Level::~Level()
{
    free(m_level_view_);
}

//----------------------------------------------------------------------------------------------------
void Level::Update(float delta)
{
    m_level_view_->Display(m_map_);
}

